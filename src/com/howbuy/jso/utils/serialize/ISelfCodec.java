package com.howbuy.jso.utils.serialize;

import java.nio.ByteBuffer;

/**
 * 该接口标识在编码和解码的时候是否使用自编码.
 * @author li.zhang
 * 2014-6-24
 *
 */
public interface ISelfCodec
{
    /**
     * 自编码
     * @return
     */
    byte[] encode();
    
    
    /**
     * 自解码
     * @param data 字节流
     */
    void decode(ByteBuffer data);
}
